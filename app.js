function drawVideoContain(video, x, y, width, height) {
  if (video.readyState < 2 || !video.videoWidth || !video.videoHeight) {
    ctx.fillStyle = "#101923";
    ctx.fillRect(x, y, width, height);
    return;
  }

  ctx.fillStyle = "#05070a";
  ctx.fillRect(x, y, width, height);

  const scale = Math.min(width / video.videoWidth, height / video.videoHeight);
  const targetWidth = video.videoWidth * scale;
  const targetHeight = video.videoHeight * scale;
  const targetX = x + (width - targetWidth) / 2;
  const targetY = y + (height - targetHeight) / 2;
  ctx.drawImage(video, targetX, targetY, targetWidth, targetHeight);
}

function drawVideoCover(video, x, y, width, height) {
  if (video.readyState < 2 || !video.videoWidth || !video.videoHeight) {
    ctx.fillStyle = "#101923";
    ctx.fillRect(x, y, width, height);
    return;
  }

  const sourceRatio = video.videoWidth / video.videoHeight;
  const targetRatio = width / height;
  let sourceWidth = video.videoWidth;
  let sourceHeight = video.videoHeight;
  let sourceX = 0;
  let sourceY = 0;

  if (sourceRatio > targetRatio) {
    sourceWidth = video.videoHeight * targetRatio;
    sourceX = (video.videoWidth - sourceWidth) / 2;
  } else {
    sourceHeight = video.videoWidth / targetRatio;
    sourceY = (video.videoHeight - sourceHeight) / 2;
  }

  ctx.drawImage(video, sourceX, sourceY, sourceWidth, sourceHeight, x, y, width, height);
}

